    public changeStatus(TaskClass data,EmployeeClass managerData) {
        initComponents();
        this.data = data;
        this.managerData=managerData;
        name.setText(data.getTitle());
        description.setText(data.getDescription());
        startDate.setText(data.getStratDate());
        dueDate.setText(data.getDueDate());

        try {
            String sql = "SELECT name FROM users WHERE ID =?";
            PreparedStatement ps;
            ps = connectionClass.connection.prepareStatement(sql);
            ps.setInt(1, data.getEmployeeId());
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                employeeName = rs.getString("name");
            }
            employee.setText(employeeName);
        } catch (SQLException ex) {
            Logger.getLogger(changeStatus.class.getName()).log(Level.SEVERE, null, ex);
        }

       try{ String sql = "SELECT name FROM users WHERE ID=?";
            PreparedStatement ps2 = connectionClass.connection.prepareStatement(sql);
            ps2.setInt(1, data.getManagerId());
            ResultSet rs2 = ps2.executeQuery();
            while (rs2.next()) {
                managerName = rs2.getString("name");
            }
            System.out.println("manager name "+managerName);
            manager.setText(managerName);
        } catch (SQLException ex) {
            Logger.getLogger(changeStatus.class.getName()).log(Level.SEVERE, null, ex);
        }

        status.addItem("Not Done ");
        status.addItem("Done");
        status.setSelectedIndex(data.getStatus());

    }

